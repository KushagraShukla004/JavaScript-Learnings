//For Loop
//Syntax
/*

for(initialization;condition;afterthought){
    statement;
}

Steps involved when a for loop is executed:

Step-1. initialization: expression that runs before the execution of the code (Simpler terms: starting of the code)
Step-2. condition: condition is checked; if true, loop executes Otherwise loop terminates(if false)
Step-3. statement(not afterthought): if condition is true ,  the actual statement where what is needed to be done is executed
Step-4. afterthought: executes after the statement is executed . Mainly used as an update expression(like x++,x-- ,--x,++x etc)
*/

